/* 

PURE VIRTUAL FUNCTIONS >>>>
1. pure virtual function in c++ is a virtual function for which we can have implementation, but we
must override that function in the derived class 
2. A pure virtual is declared by assigning 0 in the declaration ...
virtual return-data-type function-name() = 0 ;
3. if we do not override the pure virtual function in derived class, then derived class
also becomes abstract class.

ABSTRACT CLASS >>>>
1. A class is called abstract class if it has atleast one pure virtual function 
2. we can not create objects of abstract classes 
3. we can have pointers and references of abstract class type 
4. an abstract class can have constructors  

*/